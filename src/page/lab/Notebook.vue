<h3>Silicification group</h3>
<h4>8.31</h4>
<p>We tried to quantify the silica content on the surface of bacteria using this method</p>
<p>After induction by appropriate concentration of IPTG, the modulation concentration (OD600) is basically the same, siliconization is carried out in the TEOS system, and sampling is performed at regular intervals. Centrifuge and clean the silicified bacterial liquid 2-3 times, collect bacteria, and refer to the Determination of silica (dissolved) - Molybdosilicate method to detect the silica concentration on the surface of the bacteria. The experimental and control groups with different silicification times of 1 mL were transferred to centrifuge tubes for system preparation. The experiment set up three groups in parallel, and the silicification effect was judged by the measurement of the observed color and the determination of OD<sub>410</sub>.

</p>
<center>
<img src="https://static.igem.wiki/teams/4614/wiki/engineering/si/si-method.png" width="400" alt="">
<p>
<strong>Fig. Molybdosilicate method test results</strong></p></center>
<p>From the results, we can see that the OD values of the experimental and control groups are completely randomly distributed in multiple parallel experiments. After testing, there is no problem with the system. Therefore, we suspect that there is residual TEOS on the surface of the organism that has not been cleaned. After all washed three times, they were tested again and all samples did not change color.</p>
<center>
<img src="https://static.igem.wiki/teams/4614/wiki/engineering/si/si-method-clean3.png" width="300" alt="">
<p>
<strong>Fig. After increasing the number of washes, silicify for 48 h</strong></p></center>
