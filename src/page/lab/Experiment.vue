<h2>5.AGE (agarose gel electrophoresis)</h2>
<p><strong>Goal</strong> </p>
<p>Separating DNA bands according to their length, and estimating the length and DNA concentration of each band. When digested products or recombinant products are subjected to electrophoresis, care should be taken to use the original plasmids for control.</p>
<p><strong>Material</strong></p>
<p>Ready-made agarose gel；DNA sample；10 x loading buffer (dye)；DNA Ladder/Marker；1 x TAE；electrocoat tank</p>
<p><strong>Protocol</strong></p>
<p>- Place the gel on the gel tray within the electrophoresis system, make sure the wells are located at the negative electrode.</p>
<p>- Mix loading with the DNA sample if the sample is not coloured. Usually, loading has one tenth the volume of sample, but the volume of loading buffer will no less than 0.3 μl.</p>
<p>- Load either the DNA ladder or the samples within the wells. Make sure that the sample or ladder sinks to the bottom of the well. Volume of loading should between 3 μl to 5 μl, depends on the capacity of a well.</p>
<p>- Running the gel for 20-40 min, depends on the situation. Make sure the current does not exceed 75 volts.</p>
