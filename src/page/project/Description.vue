    <p>From that moment on, we embark on a journey of pure biological manufacturing.</p>
    
    
      <br><br><div class="container"><div class="horizontal-line"></div></div><h1>&nbsp;Background</h1><div class="container"><div class="horizontal-line"></div></div>
    <p>​As a major consumer of environmental resources, the materials industry urgently needs to reduce its raw material costs while considering environmental protection. Against the backdrop of this significant societal challenge, it is essential to explore technological methods capable of fundamentally transforming the processing and assembly of materials. Hence, pure biological materials have become a focal point of research. </p>
    <p>Currently, many biological materials have been developed, such as bio-composite Mycelium Bricks<sup>[1]</sup> and Cyanobacteria-based bioconcrete <sup>[2]</sup>.</p>
