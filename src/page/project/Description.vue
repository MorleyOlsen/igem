    <p>However, very few have attempted to create a biomaterial using only <i>E. coli</i>, the most common chassis bacterium in synthetic biology. CAU-China's members couldn't help but let their imaginations run wild: How could one construct a material using <i>E. coli</i> ? What surprising properties might this material possess?</p>

    <p>This represents a daring challenge. In this endeavor, each cell becomes a pixel contributing to the biomaterial creation.</p>
    
