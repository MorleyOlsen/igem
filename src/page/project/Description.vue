</center>

<h2>STEP 1 : Filamentation</h2>
<center>
    <img src="https://static.igem.wiki/teams/4614/wiki/description/sulatonglu.png" width ="400" />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    <img src="https://static.igem.wiki/teams/4614/wiki/description/sisiis.gif" width ="150" />
    <p><b>Fig. 8 Genetic circuit for filamentation (<a href="http://parts.igem.org/Part:BBa_K4614203">BBa_K4614203</a>)</b></p>
</center>
<p>When bacteria experience stress, such as DNA damage, they may activate the <i>sulA</i> gene. The SulA protein is produced in response to this stress and has the function of inhibiting a protein called FtsZ, which is crucial for normal cell division. By inhibiting FtsZ, SulA prevents the formation of the septum, and as a result, bacterial cells cannot divide into two daughter cells as they normally would. Instead, they undergo filamentation, where they elongate and remain connected.</p>
<p>Not only to achieve a rod-like structure like a magnetic rod, but also to improve the loose and porous nature of the material to make it more in line with the expected sound-absorbing properties. We designed and expressed the <i>sulA</i> gene, elongating bacterial monomers via filamentation. In addition, the initiation of filamentation means that bacterial growth and division will be inhibited, so we designed psrfA, which is a promoter of quorum sensing system, to initiate the expression of <i>sulA</i>.</p>

<h2>STEP 2 : Cross-linking</h2>

    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<img src="https://static.igem.wiki/teams/4614/wiki/wangyinuo/spytag.png" width ="400" />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
