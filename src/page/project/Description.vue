<p>      In our idea, we can control the division of <em>E. coli</em> to change their shape, thus treating them as magnetic rods and steel balls, and then cross-linking them together through two interacting proteins to form this three-dimensional network structure. This kind of structure is not uncommon in construction works, such as the famous Eiffel Tower<sup>[3]</sup>, or the structure of inorganic macromolecular materials<sup>[4]</sup> in microscopic form. This also proves the feasibility of our idea.</p>
