<p>With inspiration and ideas, we have design a specific plan to transform <i>E. coli</i>. Naturally, we divided the construction plan into three modules: filamentation, cross-linking, and Silicification. Through these three steps, the <i>E. coli</i> monomer was constructed into a new material with a three-dimensional network structure. </p>
