    <p><b>Fig. 9 Genetic circuit for cross-linking (<a href="http://parts.igem.org/Part:BBa_K4614102">BBa_K4614102</a>,<a href="http://parts.igem.org/Part:BBa_K4614117">BBa_K4614117</a>,<a href="http://parts.igem.org/Part:BBa_K4614107">BBa_K4614107</a>,<a href="http://parts.igem.org/Part:BBa_K4614108">BBa_K4614108</a>)</b></p>
</center>

<p>First we choose SpyTag and SpyCatcher as our cross-linked peptide. They are peptide sequences which have a specific and strong affinity for each other, meaning that they can form a stable covalent bond when they come into contact.</p>
<p>Just like a magnetic rod is only magnetic at both ends, next, we need to display the cross-linked peptide at specific sites on the surface of the bacteria through specific positioning peptides. We chose Wza, which is a component of <i>E.coli</i>'s surface channel protein and concentrates to the bacterial pole along with peptidoglycan as the bacteria grow, as the positioning peptide display SpyCatcher at both ends of the bacteria, and selected the flagellar L-Rings protein FlgH to display SpyTag. Spytag was captured by SpyCatcher, cross-linking bacterial cells into a regular 3D network structure.</p>

<h2>STEP 3 : Silicification</h2>
<center>
    <img src="https://static.igem.wiki/teams/4614/wiki/description/woyaokule.png" width ="400" />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    <img src="https://static.igem.wiki/teams/4614/wiki/description/jiaolian.gif" width = "150" />
    <p><b>Fig. 10 Genetic circuit for Silicification (<a href="http://parts.igem.org/Part:BBa_K4614003">BBa_K4614201003</a>)</b></p>
</center>
<p>Finally, mimicking the natural silicification process of diatoms, by surface-displaying R5 and adding TEOS, bacterial cells underwent Silicification from the outside to the inside, ensuring project safety while enhancing mechanical strength.</p>
<p>R5 is a silaffin-derived protein, which is transported by INP, a common transport protein on cell membranes encoded by LanK, to display on the outside of the cell membrane. The catalytic effect of its Lys catalytic groups on TEOS hydrolysis and silica aggregation  achieves embedding of <i>E. coli</i>
    in TEOS silicification system. INP is part of the ice nucleoprotein from <i>Pseudomonas syring</i> and considered to be the best carrier for displaying foreign proteins with large molecular weight and complex structure.
</p>

