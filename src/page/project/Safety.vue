<br><br><div class="container"><div class="horizontal-line"></div></div><h1>Safety in the production of biomaterials</h1><div class="container"><div class="horizontal-line"></div></div>
<p>​In the laboratory and future production of biomaterials in the factory, there may be concerns regarding the escape of bacterial strains. To address this issue, we have implemented corresponding measures. Firstly, our material production process involves a step called silicification, where silica peptides are expressed within the Escherichia coli strain. This leads to the formation of silica rods inside the bacteria, inducing the death of the chassis Secondly, after extracting the material from the bioreactor used in production, it undergoes high-temperature sterilization to further minimize the risk of strain escape and horizontal gene transfer. In the future, we plan to utilize nutrient-deficient host strains to further mitigate any potential leakage.</p>
<br><br><div class="container"><div class="horizontal-line"></div></div><h1>Advice We Gain</h1><div class="container"><div class="horizontal-line"></div></div>
<p>​In the siliconization step of the project, there is the use of tetraethyl silicate, is employed to deposit silica on the surface and within the host strain, enhancing material hardness. To acquire guidance from professionals regarding the usage risks and operational precautions associated with this chemical, we consulted laboratories within the school that frequently work with tetraethyl silicate. It has come to our understanding that tetraethyl silicate is flammable and may cause irritation to the eyes and respiratory system. As advised, it is recommended to wear masks, gloves, and safety glasses during experiments with this substance.</p>
