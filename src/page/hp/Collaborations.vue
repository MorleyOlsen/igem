<h1>HZAU-China</h1>
<p><strong>Communication and collaboration with the modeling team of Huazhong Agricultural University</strong></p>
<p>We had the honor of communicating and cooperating with the modeling team of Huazhong Agricultural University. Their project is to introduce synthetic bacteria that can express neutralizing antibodies against allergens into the oral mucosal microbiota of cats, in order to achieve the purpose of treating cat allergies.</p>
<p>On the evening of August 6, 2023, we exchanged the progress of modeling, shared the models we built, and gave each other beneficial suggestions and feedback through an online meeting. We shared the following three aspects of modeling content:</p>
<ul>
<li><strong>Cellular automaton model for silk initiation</strong>: We use cellular automata to simulate the quorum sensing of silk initiation.</li>
<li><strong>Simbiology simulation of silicification pathway</strong>: We use simbiology software to construct the interaction network of various molecules in the silicification pathway and perform dynamic analysis. We set various reaction rate constants and initial concentrations according to the data provided in the literature, and observed the changing trends of various molecules at different time points.</li>
<li><strong>COMSOL simulation of material sound absorption</strong>: We use COMSOL software to simulate the acoustic performance of our prepared material at different frequencies, mainly for the simulation of sound absorption coefficient and visualization of sound wave propagation.</li>

</ul>
<p>The modeling team of Huazhong Agricultural University also showed us their wonderful modeling work, mainly including:</p>
<ul>
<li><strong>Molecular docking design antibody and related expression dynamic model</strong>: They use molecular docking software to design neutralizing antibodies against cat hair allergen Fel d 1, and evaluate their affinity and stability; they use differential equations to construct a dynamic model of antibody expression and action in oral mucosal microbiota, and consider the effects of different factors on antibody expression level and activity.</li>
<li><strong>Dynamic model of suicide pathway</strong>: They construct an interaction network of various molecules in the suicide pathway and perform dynamic analysis. They set various reaction rate constants and initial concentrations according to the data provided in the literature, and observed the changing trends of various molecules as well as cell survival rate and apoptosis rate at different time points.</li>

</ul>
